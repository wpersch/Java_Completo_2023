import java.util.Locale;
import java.util.Scanner;

public class Ex06 {

	public static void main(String[] args) {
		Locale.setDefault(Locale.US);
		Scanner sc = new Scanner(System.in);
		
		System.out.println("Type three integers: ");
		float a = sc.nextFloat();
		float b = sc.nextFloat();
		float c = sc.nextFloat();
		
		float triangleArea = a * c / 2;
		double circleArea = Math.PI * Math.pow(c, 2);
		double trapezzeArea = (a + b) / 2 * c;
		double squareArea = Math.pow(b, 2);
		double rectangleArea = a *b;
		
		System.out.println();
		System.out.println("Area of");
		System.out.printf("Triangle: %.4f\n", triangleArea);
		System.out.printf("Circle: %.4f\n", circleArea);
		System.out.printf("Trapezze: %.4f\n", trapezzeArea);
		System.out.printf("Square: %.4f\n", squareArea);
		System.out.printf("Rectangle: %.4f\n", rectangleArea);
		
		
		sc.close();

	}

}
